#!/usr/bin/env bash
# Regenerate `bundle_100kb.js`, the synthetic ~100 KB user bundle
# consumed by `benches/warm_vs_cold.rs`. Run from anywhere; the
# output is written next to this script.
#
# The fixture is committed to git so contributors don't need to run
# this script to run the bench. Re-run only when:
#   - the helper-function shape needs to change (e.g. to model a
#     different bundle structure), or
#   - the target size changes.
#
# Usage:
#   ./benches/fixtures/gen-bundle.sh            # default: 2000 helpers
#   N_HELPERS=3000 ./benches/fixtures/gen-bundle.sh
#
# The size of the generated file is approximately:
#   N_HELPERS * 49 bytes/line  (one function decl per line)
#   + 1500 bytes (header + handler)
#   so 2000 helpers → ~100 KB.

set -euo pipefail

HERE="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
N="${N_HELPERS:-2000}"
OUT="$HERE/bundle_100kb.js"

{
  printf '// Synthetic ~%d KB user bundle for the warm_vs_cold benchmark.\n' "$((N / 20))"
  printf '// Generated by gen-bundle.sh — DO NOT hand-edit.\n'
  printf '//\n'
  printf '// The bench uses this as the user JS source whose lex+parse cost\n'
  printf '// dominates cold start. Re-run gen-bundle.sh with N_HELPERS=<n>\n'
  printf '// to regenerate at a different size.\n'
  printf '//\n\n'
  for ((i = 0; i < N; i++)); do
    printf 'function __helper_%d(x,y,z){return (x+%d)*(y+1)+String(z)+":h%d";}\n' "$i" "$i" "$i"
  done
  cat <<'HANDLER'

globalThis.handleRequest = function (req) {
  return {
    status: 200,
    body: JSON.stringify({ ok: true, n: req.path || "/" }),
    contentType: "application/json",
  };
};
HANDLER
} > "$OUT"

# Verify the file is at least 50 KB so a bad regeneration (e.g. N=0)
# doesn't silently shrink the fixture.
SIZE=$(wc -c < "$OUT" | tr -d ' ')
if [[ "$SIZE" -lt 51200 ]]; then
  echo "gen-bundle.sh: generated $OUT is only $SIZE bytes; expected ≥ 50 KB" >&2
  exit 1
fi

echo "wrote $OUT ($SIZE bytes, $N helpers)"
