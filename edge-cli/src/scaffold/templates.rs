/// Render an `edge.toml` by stitching the shared preamble to the
/// per-language tail. Mirrors what `crate::commands::init::write_edge_toml`
/// does at runtime — duplicated here so the tests can exercise the
/// shape without standing up a tempdir. The two-piece shape lets
/// each language carry its own `target` / `world` / `language`
/// triple while keeping the shared `name` / `version` keys in one
/// place.
#[cfg_attr(not(test), allow(dead_code))]
pub(crate) fn render_header(name: &str, lang: LangArg) -> String {
    let mut s = EDGE_TOML_PREAMBLE.replace("{name}", name);
    let tail = match lang {
        LangArg::Rust => EDGE_TOML_RUST_TAIL,
        LangArg::Js => EDGE_TOML_JS_TAIL,
    };
    s.push_str(tail);
    s
}

