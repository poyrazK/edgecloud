    // ── Rust starter (issue #576) ─────────────────────────────────────
    //
    // Three shape assertions per constant. Each constant is a piece
    // of the FaaS scaffold the user actually runs through, so the
    // tests pin:
    //
    //   1. the correct Rust `target` (`wasm32-unknown-unknown`, NOT
    //      `wasm32-wasip2` — issues #410/#414 show wasip2 emits
    //      `wasi:http@0.2.4` which wasmtime 45.0.3 rejects);
    //   2. the FaaS `world` (`edge-runtime-handler`, required by the
    //      `Project` schema at `config/edgetoml.rs:38`);
    //   3. `language = "rust"` so the scaffolded project is
    //      self-documenting.

    #[test]
    fn rust_header_pins_wasm32_unknown_unknown_and_world() {
        let result = render_header("myapp", LangArg::Rust);
        assert!(
            result.contains("target = \"wasm32-unknown-unknown\""),
            "Rust starter must target wasm32-unknown-unknown; got: {result}"
        );
        assert!(
            !result.contains("target = \"wasm32-wasip2\""),
            "Rust starter must NOT target wasm32-wasip2 (issue #410); got: {result}"
        );
        assert!(
            result.contains("world = \"edge-runtime-handler\""),
            "Rust starter must declare FaaS world; got: {result}"
        );
    }

    #[test]
    fn rust_header_includes_language_line() {
        let result = render_header("myapp", LangArg::Rust);
        assert!(
            result.contains(r#"language = "rust""#),
            "expected language = \"rust\" in: {result}"
        );
    }

